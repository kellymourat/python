for cont in range(2,51,2):
    print(cont,end= ' ')
print('Todos os números Pares, entre 1 e 50.')

#for cont in range(...) é uma forma de criar um loop que itera sobre um conjunto de números. O range() gera uma sequência numérica, e o loop for repete um bloco de código para cada número nessa sequência.

#for cont (2,51,2) in range(...) em Python significa que o código dentro do loop for será executado repetidamente para cada valor na sequência gerada pela função range(). Neste caso, a sequência é gerada começando em 2, indo até 51 (excluindo 51), e incrementando em 2 a cada passo. O valor de cada passo é atribuído à variável cont.