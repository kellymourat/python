from time import sleep
for cont in range (10, -1, -1):
     print(cont)
     sleep(1)
print('BUM! BUM! BUM! POOOW!')

#  from time import sleep significa que você está importando a função sleep do módulo time. A função sleep é usada para pausar a execução do seu programa por um determinado período de tempo, medido em segundos. 

#time: É um módulo embutido no Python que fornece funções para trabalhar com tempo.

#sleep: É uma função do módulo time que faz o programa pausar.

#from time import sleep: Importa apenas a função sleep do módulo time para que você
#possa usá-la diretamente no seu código, sem precisar referenciar o módulo. 