import emoji
print(emoji.emojize("Olá, Mundo! 🌍"))

# A instrução import emoji.emojize em Python importa o módulo emojize do pacote emoji. O emojize é uma função que converte texto que contém aliases de emoji (como :smile:) em emojis reais (como 😊). Isso é útil para gerar e exibir emojis em programas Python.