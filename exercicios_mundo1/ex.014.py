import math
num = int(input('Digite um número:'))
raiz = math.sqrt(num)
print ('Raiz {} = {}'.format(num,math.floor(raiz)))

# import math significa que você está a importar o módulo math, que contém uma variedade de funções matemáticas avançadas. Isso permite que você utilize essas funções em seu código, como raiz quadrada, exponenciais, logaritmos, funções trigonométricas, etc. 